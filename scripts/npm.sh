#!/bin/zsh

# npm
alias npms="npm start"
alias istart="npm i --no-save && npm start"
alias idev="npm i --no-save && npm run dev"
alias istorybook="npm i --no-save && npm run storybook"
alias ibuild="npm i --no-save && npm run build"

# yarn
alias dev="yarn dev"
alias start="yarn start"
alias build="yarn build"
alias ydev="yarn install --frozen-lockfile && yarn dev"
alias ystart="yarn install --frozen-lockfile && yarn start"
alias ybuild="yarn install --frozen-lockfile && yarn build"
